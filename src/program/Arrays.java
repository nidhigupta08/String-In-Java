package program;

public class Arrays {
	 public static void main(String[] args) {
	        int[] numbers = { 1, 2, 3, 4, 5 };

	        System.out.println("Elements in the array:");
	        for (int number : numbers) {
	            System.out.print(number + " ");
	        }
	        System.out.println();
	    }
}
